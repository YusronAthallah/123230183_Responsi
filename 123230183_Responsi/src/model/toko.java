package model;

public class toko {
    private int ID;
    private String Nama;
    private String Barang;
    private int Harga;
    private String Cicilan;
    private int Bunga;
    private int Angsuran;
    private int Total;
    
    public int getID(){
        return ID;
    }
    
    public void setID(int ID){
        this.ID = ID;
    }
    
    public String getNama(){
        return Nama;
    }
    
    public void setID(String Nama){
        this.Nama = Nama;
    }
    
    public String getBarang(){
        return Barang;
    }
    
    public void setBarang(String Barang){
        this.Barang = Barang;
    }
    
    public int getHarga(){
        return Harga;
    }
    
    public void setHarga(int Harga){
        this.Harga = Harga;
    }
    
    public String getCicilan(){
        return Cicilan;
    }
    
    public void setCicilan(String Cicilan){
        this.Cicilan = Cicilan;
    }
    
    public int getBunga(){
        return Bunga;
    }
    
    public void setBunga(int Bunga){
        this.Bunga = Bunga;
    }
    
    public int getAngsuran(){
        return ID;
    }
    
    public void setAngsuran(int Angsuran){
        this.Angsuran = Angsuran;
    }
    
    public int getTotal(){
        return Total;
    }
    
    public void setTotal(int Total){
        this.Total = Total;
    }
}